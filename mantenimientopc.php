            <h3 class="color3">Mantenimiento Preventivo</h3><hr>
            <p align="justify" class="color3">Es todo aquello que hacemos con el propósito de mantener nuestro ordenador funcionando de la manera más satisfactoria posible. Para esto hay que hacer una que otra prueba de vez en cuando, mientras más sistemáticos seamos más indoloro es el proceso. Todo con el objetivo de evitar la mayor cantidad de problemas, o de mitigar el daño en caso de una falla.</p>
                        <br>
                        <img src="img/preventivo.jpg" class="img-responsive img-thumbnail" alt="Responsive image">
                         <br>
                         <p align="justify"class="color3">Hace algún tiempo les contamos sobre algunas herramientas que podemos usar para realizar mantenimiento preventivo en Windows. Pero incluso sin usar ninguna de estas (aunque altamente recomendadas), podemos realizar ciertas tareas para mantener nuestro PC a punto:</p>
                         <ul class="color3"> 
                         <li> Desfragmentar los discos duros</li>
                          <li>Realizar respaldos de los datos almacenados.</li>
                          <li>Instalar las actualizaciones de seguridad de nuestro sistema operativo.</li>
                          <li>Mantener nuestras aplicaciones actualizadas.</li>
                           <li>No instalar software inestable o de dudosa procedencia.</li>
                          <li>Navegar la web de manera responsable.</li>
                          <li>Limpiar nuestros datos de navegación cada cierto tiempo.</li>
                          <li>Instalar alguna herramienta antimalware y hacer un scan periódicamente.</li>
                          <li>Administrar los programas que inician con el sistema y remover los que sean innecesarios.</li>
                          <li>Monitorear el consumo de recursos de las aplicaciones para saber cuando es necesario un upgrade de nuestro hardware.</li>
                          <li>Vaciar la papelera de reciclaje, por más tonto que parezca.</li>
                         </ul>
                          <br>

                          <p align="justify" class="color3">Muchas de estas recomendaciones aplican para realizar mantenimiento en la mayoría de los sistemas operativos, aunque siempre especial cuidado con el malware en Windows, ya que ataca de todos lados.</p>
<br>
<p align="justify" class="color3">En el caso del hardware, también podemos tomar un par de medidas para evitar problemas. Si tienes una PC de escritorio mantener limpio el case por dentro y por fuera para evitar que se bloquee la ventilación puede prevenir un fallo grave en tu procesador por sobrecalentamiento. Tener un soplador o una lata de aire comprimido puede ayudar mucho. Si eres más diestro, y notas que la temperatura de tu CPU está muy alta, puedes revisar la pasta térmica del procesador, reemplazarla no es difícil y se consigue en casi cualquier tienda de computación. Si te animas, nuestros compañeros de Gizmología explicaron paso a paso como aplicar pasta térmica.</p>

                        
                    </div>
                                            
                       <div class="col-xs-12 col-sm-12 col-md-6 col-lg-6">
                           <h3 class="color3">Mantenimiento Correctivo</h3><hr>
                           <p align="justify" class="color3">Este es el que hacemos cuando algo falla, una vez que se diagnostica el problema se busca una manera de solucionarlo. Me gusta comparar los problemas de los ordenadores con los de salud de la gente, si tienes una falla de hardware te mandan al cirujano, te arreglan las piezas malas y si no hay remedio, pues al cementerio, tal vez algunos órganos funcionen y puedan ser donados a otras PCs.</p>
                        
                        <img src="img/correctivo.jpg" class="img-responsive img-thumbnail" alt="Responsive image">
                        <p align="justify" class="color3">Si tienes un problema de software se te da un tratamiento medico, nadie te va a abrir el case, sino que instalas algunas medicinas, y tomas medidas para prevenir recaídas. La mayoría de las veces, los problemas de un ordenador son de software: malware, aplicaciones que consumen muchos recursos, demasiado archivos basura ocupando espacio en disco, paquetes rotos, falta de actualizaciones,drivers obsoletos, etc., cosas que en su mayoría pueden prevenirse, y no son tan difíciles de resolver.</p>
                        <p align="justify" class="color3">Cuando las fallas son de hardware los síntomas usualmente son más agresivos: pantallazos azules, kernel panic, cuelgues repentinos, sonidos extraños, el ordenador se apaga solo, o ya de cajón no prende. Los problemas de hardware son mucho más delicados, ya que lo peor que puede pasarte con un grave problema de software es que tengas que formatear el ordenador, pero si te falla el cuerpo de la máquina puedes llegar a perderla. Así que lo más recomendado es buscar ayuda de alguien que sepa, en lugar de inventar mucho por tu cuenta si no tienes pericia.</p>

                        
                        
